import { randomUUID } from "node:crypto";
import { mkdir, readFile, rename, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const repositoryRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
);
const sourceRelativePath = "policy/provider-activation-profiles.v1.json";
const outputRelativePaths = [
  "packages/contracts/src/provider-activation-profiles.ts",
];
const invalidSourceError = "Invalid provider activation profile source";
const generationError = "Provider activation profile generation failed";
const profileID = /^[a-z][a-z0-9]*(?:_[a-z0-9]+)*_v[1-9][0-9]*$/u;
const providerID = /^[a-z0-9]+(?:-[a-z0-9]+)*$/u;
const maximumActivationProfileIDBytes = 64;

function invalidSource() {
  return new Error(invalidSourceError);
}

function exactKeys(value, expected) {
  if (typeof value !== "object" || value === null || Array.isArray(value)) {
    throw invalidSource();
  }
  const actual = Object.keys(value).sort();
  const wanted = [...expected].sort();
  if (
    actual.length !== wanted.length
    || actual.some((key, index) => key !== wanted[index])
  ) {
    throw invalidSource();
  }
}

async function readCanonicalSource(rootDirectory) {
  let sourceText;
  try {
    sourceText = await readFile(
      path.join(rootDirectory, sourceRelativePath),
      "utf8",
    );
  } catch {
    throw invalidSource();
  }

  let source;
  try {
    source = JSON.parse(sourceText);
  } catch {
    throw invalidSource();
  }

  exactKeys(source, ["schemaVersion", "profiles"]);
  if (
    source.schemaVersion !== 1
    || !Array.isArray(source.profiles)
    || source.profiles.length === 0
    || source.profiles.length > 256
  ) {
    throw invalidSource();
  }

  const profiles = source.profiles.map((profile) => {
    exactKeys(profile, ["id", "bundledProviderId"]);
    if (
      typeof profile.id !== "string"
      || profile.id.length > maximumActivationProfileIDBytes
      || !profileID.test(profile.id)
      || (
        profile.bundledProviderId !== null
        && (
          typeof profile.bundledProviderId !== "string"
          || profile.bundledProviderId.length > 128
          || !providerID.test(profile.bundledProviderId)
        )
      )
    ) {
      throw invalidSource();
    }
    return {
      id: profile.id,
      bundledProviderId: profile.bundledProviderId,
    };
  });

  const ids = profiles.map(({ id }) => id);
  const bundledProviderIDs = profiles.flatMap(({ bundledProviderId }) =>
    bundledProviderId === null ? [] : [bundledProviderId]
  );
  if (
    new Set(ids).size !== ids.length
    || ids.some((id, index) => index > 0 && ids[index - 1] >= id)
    || new Set(bundledProviderIDs).size !== bundledProviderIDs.length
  ) {
    throw invalidSource();
  }

  const canonicalSource = `${JSON.stringify(
    { schemaVersion: 1, profiles },
    null,
    2,
  )}\n`;
  if (sourceText !== canonicalSource) {
    throw invalidSource();
  }
  return profiles;
}

function typescript(profiles) {
  const catalog = profiles.map((profile) =>
    `  Object.freeze(${JSON.stringify(profile)}),`
  ).join("\n");
  const mappings = profiles.flatMap(({ id, bundledProviderId }) =>
    bundledProviderId === null
      ? []
      : [`  ${JSON.stringify(bundledProviderId)}: ${JSON.stringify(id)},`]
  ).join("\n");
  return `// Generated by scripts/generate-provider-activation-profiles.mjs. Do not edit.

export const PROVIDER_ACTIVATION_PROFILES = Object.freeze([
${catalog}
] as const);

export type ProviderActivationProfileId =
  (typeof PROVIDER_ACTIVATION_PROFILES)[number]["id"];

export const BUNDLED_PROVIDER_ACTIVATION_PROFILE_IDS = Object.freeze({
${mappings}
} as const);
`;
}

async function outputsAreCurrent(outputs) {
  const comparisons = await Promise.all(
    outputs.map(async ({ outputPath, contents }) => {
      try {
        return (await readFile(outputPath, "utf8")) === contents;
      } catch {
        return false;
      }
    }),
  );
  return comparisons.every(Boolean);
}

function isMissingFile(error) {
  return typeof error === "object" && error !== null && error.code === "ENOENT";
}

async function writeOutputs(outputs, renameFile) {
  const staged = [];
  const backups = [];
  const installed = [];
  const retainedBackups = new Set();
  try {
    for (const { outputPath, contents } of outputs) {
      const directory = path.dirname(outputPath);
      await mkdir(directory, { recursive: true });
      const temporaryPath = path.join(
        directory,
        `.${path.basename(outputPath)}.${process.pid}.${randomUUID()}.tmp`,
      );
      await writeFile(temporaryPath, contents, {
        encoding: "utf8",
        flag: "wx",
      });
      staged.push({ outputPath, temporaryPath });
    }
    for (const { outputPath } of staged) {
      const backupPath = path.join(
        path.dirname(outputPath),
        `.${path.basename(outputPath)}.${process.pid}.${randomUUID()}.bak`,
      );
      try {
        await renameFile(outputPath, backupPath);
        backups.push({ outputPath, backupPath });
      } catch (error) {
        if (!isMissingFile(error)) throw error;
      }
    }
    for (const { outputPath, temporaryPath } of staged) {
      await renameFile(temporaryPath, outputPath);
      installed.push(outputPath);
    }
  } catch {
    for (const outputPath of installed.reverse()) {
      await rm(outputPath, { force: true }).catch(() => undefined);
    }
    for (const { outputPath, backupPath } of [...backups].reverse()) {
      await renameFile(backupPath, outputPath).catch(() => {
        retainedBackups.add(backupPath);
      });
    }
    throw new Error(generationError);
  } finally {
    await Promise.all(
      [
        ...staged.map(({ temporaryPath }) => temporaryPath),
        ...backups
          .map(({ backupPath }) => backupPath)
          .filter((backupPath) => !retainedBackups.has(backupPath)),
      ].map(async (artifactPath) =>
        rm(artifactPath, { force: true }).catch(() => undefined)
      ),
    );
  }
}

/**
 * @param {{ rootDirectory: string, check: boolean }} options
 * @param {{ renameFile?: (source: string, destination: string) => Promise<void> }} [dependencies]
 * @returns {Promise<boolean>}
 */
export async function generateProviderActivationProfiles({
  rootDirectory,
  check,
}, {
  renameFile = rename,
} = {}) {
  const profiles = await readCanonicalSource(rootDirectory);
  const contents = [typescript(profiles)];
  const outputs = outputRelativePaths.map((relativePath, index) => ({
    outputPath: path.join(rootDirectory, relativePath),
    contents: contents[index],
  }));
  if (check) {
    return outputsAreCurrent(outputs);
  }
  await writeOutputs(outputs, renameFile);
  return true;
}

async function run() {
  const arguments_ = process.argv.slice(2);
  if (
    arguments_.length > 1
    || (arguments_.length === 1 && arguments_[0] !== "--check")
  ) {
    throw new Error(generationError);
  }
  const current = await generateProviderActivationProfiles({
    rootDirectory: repositoryRoot,
    check: arguments_[0] === "--check",
  });
  if (!current) {
    process.stderr.write("Generated provider activation profile files are stale\n");
    process.exitCode = 1;
  }
}

const invokedPath = process.argv[1] ? path.resolve(process.argv[1]) : undefined;
if (invokedPath === fileURLToPath(import.meta.url)) {
  try {
    await run();
  } catch {
    process.stderr.write(`${generationError}\n`);
    process.exitCode = 1;
  }
}
