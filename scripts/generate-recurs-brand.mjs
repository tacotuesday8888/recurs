import console from "node:console";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

import { RECURS_BRAND } from "./recurs-brand.mjs";

const rootDirectory = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const check = process.argv.includes("--check");

const GENERATED_HEADER = "Generated by scripts/generate-recurs-brand.mjs. Do not edit.";

function typescriptSource() {
  return `// ${GENERATED_HEADER}\n\nexport const RECURS_TERMINAL_ROWS = Object.freeze(${JSON.stringify(RECURS_BRAND.terminalRows)});\n\nexport const RECURS_MARK_ANSI_256 = Object.freeze(${JSON.stringify(RECURS_BRAND.terminalAnsi256)});\n`;
}

function markSvg() {
  const { palette, strokes, arrowPoints } = RECURS_BRAND;
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 160 160" role="img" aria-labelledby="title description">
  <title id="title">Recurs</title>
  <desc id="description">A returning loop shaped like a letter R.</desc>
  <!-- ${GENERATED_HEADER} -->
  <defs>
    <linearGradient id="recurs-gradient" x1="0" y1="0" x2="1" y2="1">
      <stop offset="0" stop-color="${palette.blue}"/>
      <stop offset="0.5" stop-color="${palette.cyan}"/>
      <stop offset="1" stop-color="${palette.mint}"/>
    </linearGradient>
  </defs>
  <g fill="none" stroke="url(#recurs-gradient)" stroke-linecap="round" stroke-linejoin="round">
${strokes.map(({ d, width }) => `    <path d="${d}" stroke-width="${width}"/>`).join("\n")}
  </g>
  <polygon points="${arrowPoints}" fill="url(#recurs-gradient)"/>
</svg>
`;
}

function terminalPreviewSvg() {
  const { palette, strokes, arrowPoints } = RECURS_BRAND;
  return `<svg xmlns="http://www.w3.org/2000/svg" width="760" height="170" viewBox="0 0 760 170" role="img" aria-labelledby="title description">
  <title id="title">Welcome to Recurs</title>
  <desc id="description">The Recurs loop mark beside a terminal welcome message.</desc>
  <!-- ${GENERATED_HEADER} -->
  <defs>
    <linearGradient id="recurs-gradient" x1="0" y1="0" x2="1" y2="1">
      <stop offset="0" stop-color="${palette.blue}"/>
      <stop offset="0.5" stop-color="${palette.cyan}"/>
      <stop offset="1" stop-color="${palette.mint}"/>
    </linearGradient>
    <style>
      .muted { fill: ${palette.muted}; font: 16px ui-monospace, SFMono-Regular, Menlo, Consolas, monospace; }
      .strong { fill: ${palette.foreground}; font: 800 29px ui-monospace, SFMono-Regular, Menlo, Consolas, monospace; }
    </style>
  </defs>
  <rect width="760" height="170" rx="16" fill="${palette.background}"/>
  <g transform="translate(28 13) scale(0.9)">
    <g fill="none" stroke="url(#recurs-gradient)" stroke-linecap="round" stroke-linejoin="round">
${strokes.map(({ d, width }) => `      <path d="${d}" stroke-width="${width}"/>`).join("\n")}
    </g>
    <polygon points="${arrowPoints}" fill="url(#recurs-gradient)"/>
  </g>
  <text x="190" y="76" class="strong">Welcome to Recurs</text>
  <text x="190" y="111" class="muted">A bounded agent company for your terminal.</text>
</svg>
`;
}

const outputs = new Map([
  ["packages/cli/src/generated/recurs-brand.ts", typescriptSource()],
  ["docs/assets/recurs-mark.svg", markSvg()],
  ["docs/assets/terminal-preview.svg", terminalPreviewSvg()],
]);

let stale = false;
for (const [relativePath, content] of outputs) {
  const outputPath = path.join(rootDirectory, relativePath);
  if (check) {
    const current = await readFile(outputPath, "utf8").catch(() => "");
    if (current !== content) {
      console.error(`${relativePath} is stale; run npm run generate:recurs-brand`);
      stale = true;
    }
    continue;
  }
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, content, "utf8");
}

if (stale) process.exitCode = 1;
