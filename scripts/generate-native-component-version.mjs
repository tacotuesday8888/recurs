import { randomUUID } from "node:crypto";
import { mkdir, readFile, rename, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const repositoryRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
);
const sourceRelativePath = "native/component-version.json";
const outputRelativePaths = [
  "packages/contracts/src/native-component-version.ts",
  "native/macos/Sources/RecursNativeProtocol/GeneratedNativeComponentVersion.swift",
  "native/macos/Resources/RecursBroker-Info.plist",
  "native/macos/Resources/RecursLauncher-Info.plist",
];
const invalidSourceError = "Invalid native component version source";
const generationError = "Native component version generation failed";
const numericIdentifier = "(?:0|[1-9][0-9]*)";
const nativeComponentVersion = new RegExp(
  `^${numericIdentifier}\\.${numericIdentifier}\\.${numericIdentifier}$`,
);

function invalidSource() {
  return new Error(invalidSourceError);
}

async function readCanonicalSource(rootDirectory) {
  let sourceText;
  try {
    sourceText = await readFile(
      path.join(rootDirectory, sourceRelativePath),
      "utf8",
    );
  } catch {
    throw invalidSource();
  }

  let source;
  try {
    source = JSON.parse(sourceText);
  } catch {
    throw invalidSource();
  }
  if (typeof source !== "object" || source === null || Array.isArray(source)) {
    throw invalidSource();
  }
  const keys = Object.keys(source).sort();
  if (keys.length !== 2 || keys[0] !== "schemaVersion" || keys[1] !== "version") {
    throw invalidSource();
  }
  if (
    source.schemaVersion !== 1
    || typeof source.version !== "string"
    || source.version.length > 256
    || !nativeComponentVersion.test(source.version)
  ) {
    throw invalidSource();
  }
  const canonicalSource = `${JSON.stringify(
    { schemaVersion: 1, version: source.version },
    null,
    2,
  )}\n`;
  if (sourceText !== canonicalSource) {
    throw invalidSource();
  }
  return source.version;
}

function typescript(version) {
  return `// Generated by scripts/generate-native-component-version.mjs. Do not edit.\n\nexport const NATIVE_COMPONENT_VERSION = ${JSON.stringify(version)};\n`;
}

function swift(version) {
  return `// Generated by scripts/generate-native-component-version.mjs. Do not edit.\n\npublic enum NativeComponentVersion {\n  public static let current = ${JSON.stringify(version)}\n}\n`;
}

function plist(source, version) {
  const key = "\t<key>CFBundleShortVersionString</key>\n";
  const prefix = `${key}\t<string>`;
  const start = source.indexOf(prefix);
  if (start < 0 || source.indexOf(key, start + key.length) >= 0) {
    throw new Error(generationError);
  }
  const valueStart = start + prefix.length;
  const valueEnd = source.indexOf("</string>", valueStart);
  const current = valueEnd < 0 ? "" : source.slice(valueStart, valueEnd);
  if (
    current.length === 0 ||
    current.includes("<") ||
    current.includes("\n") ||
    current.includes("\r")
  ) {
    throw new Error(generationError);
  }
  return source.slice(0, valueStart) + version + source.slice(valueEnd);
}

async function generatedContents(rootDirectory, version) {
  let brokerPlist;
  let launcherPlist;
  try {
    [brokerPlist, launcherPlist] = await Promise.all(
      outputRelativePaths.slice(2).map(async (relativePath) =>
        readFile(path.join(rootDirectory, relativePath), "utf8")
      ),
    );
  } catch {
    throw new Error(generationError);
  }
  return [
    typescript(version),
    swift(version),
    plist(brokerPlist, version),
    plist(launcherPlist, version),
  ];
}

async function outputsAreCurrent(outputs) {
  const comparisons = await Promise.all(
    outputs.map(async ({ outputPath, contents }) => {
      try {
        return (await readFile(outputPath, "utf8")) === contents;
      } catch {
        return false;
      }
    }),
  );
  return comparisons.every(Boolean);
}

async function writeOutputs(outputs) {
  const staged = [];
  try {
    for (const { outputPath, contents } of outputs) {
      const directory = path.dirname(outputPath);
      await mkdir(directory, { recursive: true });
      const temporaryPath = path.join(
        directory,
        `.${path.basename(outputPath)}.${process.pid}.${randomUUID()}.tmp`,
      );
      await writeFile(temporaryPath, contents, {
        encoding: "utf8",
        flag: "wx",
      });
      staged.push({ outputPath, temporaryPath });
    }
    for (const { outputPath, temporaryPath } of staged) {
      await rename(temporaryPath, outputPath);
    }
  } catch {
    throw new Error(generationError);
  } finally {
    await Promise.all(
      staged.map(async ({ temporaryPath }) =>
        rm(temporaryPath, { force: true }).catch(() => undefined),
      ),
    );
  }
}

/**
 * @param {{ rootDirectory: string, check: boolean }} options
 * @returns {Promise<boolean>}
 */
export async function generateNativeComponentVersion({
  rootDirectory,
  check,
}) {
  const version = await readCanonicalSource(rootDirectory);
  const contents = await generatedContents(rootDirectory, version);
  const outputs = outputRelativePaths.map((relativePath, index) => ({
    outputPath: path.join(rootDirectory, relativePath),
    contents: contents[index],
  }));
  if (check) {
    return outputsAreCurrent(outputs);
  }
  await writeOutputs(outputs);
  return true;
}

async function run() {
  const arguments_ = process.argv.slice(2);
  if (
    arguments_.length > 1
    || (arguments_.length === 1 && arguments_[0] !== "--check")
  ) {
    throw new Error(generationError);
  }
  const current = await generateNativeComponentVersion({
    rootDirectory: repositoryRoot,
    check: arguments_[0] === "--check",
  });
  if (!current) {
    process.stderr.write("Generated native component version files are stale\n");
    process.exitCode = 1;
  }
}

const invokedPath = process.argv[1] ? path.resolve(process.argv[1]) : undefined;
if (invokedPath === fileURLToPath(import.meta.url)) {
  try {
    await run();
  } catch {
    process.stderr.write(`${generationError}\n`);
    process.exitCode = 1;
  }
}
